export function createTaskId(): string {
  if (typeof crypto !== 'undefined' && crypto.randomUUID) return `task-${crypto.randomUUID()}`
  return `task-${Date.now()}-${Math.random().toString(36).slice(2, 9)}`
}

export function createTaskLogId(): string {
  if (typeof crypto !== 'undefined' && crypto.randomUUID) return `tlog-${crypto.randomUUID()}`
  return `tlog-${Date.now()}-${Math.random().toString(36).slice(2, 9)}`
}

